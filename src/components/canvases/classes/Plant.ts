type PlantProps = {
  sideLength: number;
  x: number;
  y: number;
  map?: number[][];
  delay?: number;
  opacity?: number;
  parallaxStrength?: number;
};

function generateTreeMap(height: number, width: number): number[][] {
  const map: number[][] = Array.from({ length: height }, () =>
    Array(width).fill(0),
  );

  const centerX = Math.floor(width / 2);
  let stage = 1;

  // Build the trunk
  for (let y = height - 1; y >= height - 6; y--) {
    map[y][centerX] = stage++;
  }

  // Branch logic
  const branchStartY = height - 6;
  for (let y = branchStartY; y >= 0; y--) {
    const spread = Math.floor((height - branchStartY) / 2) + 2;
    const branchWidth = Math.floor(Math.sin((branchStartY - y) / 2) * spread);

    for (let dx = -branchWidth; dx <= branchWidth; dx++) {
      const x = centerX + dx;
      if (x >= 0 && x < width && Math.random() > 0.2) {
        map[y][x] = stage++;
      }
    }
  }

  return map;
}

export class Plant {
  currentFrame = 0;
  swayAngle = 0;
  opacity = 1;
  // 0/5 low, 5 high ? this cool setting
  //remember 3 px 3 intensity ver fun
  swayIntensity = 0.8;
  scale = 1;
  sideLength = 1;
  growthPerSecond = 100;
  delay = 0;
  map: Array<Array<number>> = [];
  currentStage = 2;
  x = 0;
  y = 0;
  originalX = 0;
  originalY = 0;
  shiftX = 0;
  shiftY = 0;
  lastDeltaFrameTime = 0;
  timeSinceLastFrame = 0;
  baseColor = "transparent"; //'#5A3E1B';
  mainColor = "#84ffd6";
  accentColor = "#6a8eff";
  accentTile = [0, 0];
  parallaxStrength = 0.01;

  constructor(props: PlantProps) {
    const {
      sideLength = 1,
      x = 0,
      y = 0,
      map = ForegroundVine,
      delay = 0,
      opacity = 1,
      parallaxStrength = 0.01,
    } = props;
    this.sideLength = sideLength;
    this.x = x;
    this.y = y;
    this.originalX = x;
    this.originalY = y;
    this.map = map;
    this.delay = delay;
    this.opacity = opacity;
    this.parallaxStrength = parallaxStrength;
  }

  getWaveOffset(colIndex: number, rowIndex: number, divider = 1) {
    return (
      (Math.sin(this.swayAngle + rowIndex * 0.4) *
        Math.cos(this.swayAngle * 0.3 + colIndex * 0.7) *
        this.sideLength *
        this.swayIntensity) /
      divider
    ); // proportional sway;
  }

  draw(ctx: CanvasRenderingContext2D, canvas: HTMLCanvasElement) {
    const { map, currentStage, sideLength, swayAngle } = this;
    ctx.save();
    ctx.globalAlpha = this.opacity;
    ctx.fillStyle = "green";
    for (let rowIndex = 0; rowIndex < map.length; rowIndex++) {
      const row = map[map.length - 1 - rowIndex];
      const y = this.y - (rowIndex + 1) * sideLength;
      row.forEach((cell, colIndex) => {
        if (cell && cell < currentStage) {
          const waveOffset = this.getWaveOffset(colIndex, rowIndex);
          const x = colIndex * sideLength + waveOffset + this.x;
          ctx.fillStyle = this.mainColor;
          ctx.fillRect(x, y, sideLength, sideLength);
        } else if (!cell) {
          const waveOffset = this.getWaveOffset(colIndex, rowIndex, 4);
          const x = colIndex * sideLength + waveOffset + this.x;
          ctx.fillStyle = this.baseColor;
          ctx.fillRect(x, y, sideLength, sideLength);
        }
      });
    }
    ctx.restore();
  }

  update(time = 0, pos?: { xPos: number; yPos: number }) {
    const delta = time - this.lastDeltaFrameTime;

    this.timeSinceLastFrame += delta;
    if (time > this.delay && this.timeSinceLastFrame > this.growthPerSecond) {
      this.timeSinceLastFrame = 0;
      this.currentStage++;
    }
    this.lastDeltaFrameTime = time;
    this.swayAngle += delta * 0.0018; // slower and smoother

    // ⬇️ Add this parallax code
    if (pos) {
      const centerX = window.innerWidth / 2;
      const centerY = window.innerHeight / 2;

      const dx = (pos.xPos - centerX) * this.parallaxStrength;
      const dy = (pos.yPos - centerY) * this.parallaxStrength;

      this.x = this.originalX + dx;
      this.y = this.originalY + dy;
    }
  }
}

